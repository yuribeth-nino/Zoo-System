public class Keeper {

    private String name;
    private int id;
    // private String Qualified ; 

    /**
     *
     * @param name
     * @param id
     */
    public Keeper(String name, int id) {

        this.name = name;
        this.id = id;
    }

    /**
     *
     * @return
     */
    public String getName() {
        return name;
    }

    /**
     *
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     *
     * @return
     */
    public int getId() {
        return id;
    }

    /**
     *
     * @param id
     */
    public void setId(int id) {
        this.id = id;
    }

}
